BEGIN
  EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM DPV001."BỆNHNHÂN" FOR SYS."BỆNHNHÂN"'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN
  EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM DPV001."HSBA" FOR SYS."HSBA"'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN
  EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM DPV001."HSBA_DV" FOR SYS."HSBA_DV"'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN
  EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM DPV001."ĐƠNTHUỐC" FOR SYS."ĐƠNTHUỐC"'; EXCEPTION WHEN OTHERS THEN NULL; END;
