BEGIN
  EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM DPV001."THÔNGBÁO" FOR SYS."THÔNGBÁO"'; EXCEPTION WHEN OTHERS THEN NULL; END;
