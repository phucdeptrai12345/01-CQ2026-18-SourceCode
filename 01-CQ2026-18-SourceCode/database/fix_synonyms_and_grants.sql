BEGIN EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM BS003."NHÂNVIÊN" FOR SYS."NHÂNVIÊN"'; EXCEPTION WHEN OTHERS THEN NULL; END;/
BEGIN EXECUTE IMMEDIATE 'CREATE OR REPLACE SYNONYM BS003."BỆNHNHÂN" FOR SYS."BỆNHNHÂN"'; EXCEPTION WHEN OTHERS THEN NULL; END;/
