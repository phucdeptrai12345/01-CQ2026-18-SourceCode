-- fix_synonyms_and_grants.sql
-- Chạy bằng SYS (AS SYSDBA) trong PDB (ví dụ XEPDB1)
-- Mục đích: tạo private synonyms cho các user ứng dụng và cấp quyền SELECT cần thiết

SET ECHO ON;

-- Danh sách user cần tạo synonym/grant
-- DPV001, DPV002, BS001..BS003, KTV001..KTV002, BN001..BN005

BEGIN
  NULL; -- chỉ để tránh lỗi khi chạy như script trong SQL Developer
END;
/
-- Tạo private synonyms: user."OBJECT" FOR SYS."OBJECT"
