-- Lặp grant cho các user khác (DPV, KTV, BS002..)
BEGIN EXECUTE IMMEDIATE 'GRANT SELECT ON SYS."NHÂNVIÊN" TO BS002'; EXCEPTION WHEN OTHERS THEN NULL; END;/
BEGIN EXECUTE IMMEDIATE 'GRANT SELECT ON SYS."BỆNHNHÂN" TO BS002'; EXCEPTION WHEN OTHERS THEN NULL; END;/
